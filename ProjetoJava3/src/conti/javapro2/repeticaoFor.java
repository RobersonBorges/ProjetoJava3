package conti.javapro2;

public class repeticaoFor {
	public static void main (String[] args) {
		/* estrutura de repetição FOR*/
		/* inicializando variavel, depois diz ate onde vai, depois intervalo*/
		for (int numero = 0; numero <= 10; numero++) {
			System.out.println("o numero atual e: " +numero);
		}
		for (int numero2 = 10; numero2 >= 0; numero2--) {
			System.out.println("o numero atual e: " +numero2);
		}
	}

}
